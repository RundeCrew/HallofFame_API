package co.grandcircus.HallOfFame.model;

import java.util.List;

public class Results {

	private List<Complete> completes;
	private List<Tiny> tinys;
	
	

	public Results() {
		super();
	}



	public List<Complete> getCompletes() {
		return completes;
	}



	public void setCompletes(List<Complete> completes) {
		this.completes = completes;
	}



	public List<Tiny> getTinys() {
		return tinys;
	}



	public void setTinys(List<Tiny> tinys) {
		this.tinys = tinys;
	}



	
	
	
}
